
import { GoogleGenAI } from "@google/genai";

const ai = new GoogleGenAI({ apiKey: process.env.API_KEY });

export const getTravelAdvice = async (userMessage: string, chatHistory: {role: string, content: string}[]) => {
  const model = 'gemini-3-flash-preview';
  
  const systemInstruction = `
    Ты — ведущий юрист компании "Исланд Восток" (Владивосток). 
    Твоя специализация: Автоправо (защита водителей), Морское, Таможенное и Налоговое право.
    Твой тон: Профессиональный, серьезный, внушающий доверие, но готовый защищать клиента до конца.
    
    Ключевые факты для ответов:
    - Опыт компании: более 25 лет.
    - Работаем по официальному договору.
    - Офис: Владивосток, ул. Русская 65/4.
    - Помогаем при лишении прав (ст. 12.8, 12.9, 12.26, 12.27 КоАП РФ).
    - Мы не автошкола (не помогаем со сдачей экзаменов в ГИБДД).
    - Консультация по телефону — бесплатно.
    
    Если клиент спрашивает про лишение прав, объясни, что мы анализируем протоколы на наличие процессуальных ошибок и помогаем вернуть права в суде.
    Если вопрос касается цен, отвечай, что базовая консультация стоит от 500 рублей, а ведение дела рассчитывается индивидуально.
    Всегда предлагай оставить номер телефона или написать в WhatsApp для детального разбора дела.
  `;

  try {
    const response = await ai.models.generateContent({
      model,
      contents: [
        ...chatHistory.map(m => ({ role: m.role === 'user' ? 'user' : 'model', parts: [{ text: m.content }] })),
        { role: 'user', parts: [{ text: userMessage }] }
      ],
      config: {
        systemInstruction,
        temperature: 0.3,
      }
    });

    return response.text || "Юрист сейчас на судебном заседании. Оставьте ваш вопрос и телефон, мы свяжемся с вами в ближайшее время.";
  } catch (error) {
    console.error("Gemini Legal Error:", error);
    return "Произошла техническая ошибка. Пожалуйста, позвоните нам напрямую: +7 (908) 978-89-19.";
  }
};
